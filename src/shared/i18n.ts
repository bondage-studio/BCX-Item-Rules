export type I18nKey = keyof typeof EN;
type I18nValues = Record<string, string | number | boolean | null | undefined>;

export function getI18nLanguage(root: HostWindow): "en" | "zh-CN" {
  const candidates = [
    getFunctionLanguage(root),
    root.TranslationLanguage,
    root.PreferenceLanguage,
    root.Player?.Language,
    root.Player?.OnlineSettings?.Language,
    root.navigator?.language,
  ];
  const language = candidates.find((candidate) => typeof candidate === "string" && candidate.trim()) || "en";
  return /^zh\b|^cn\b/i.test(language) ? "zh-CN" : "en";
}

export function t(root: HostWindow, key: I18nKey, values: I18nValues = {}): string {
  const table = getI18nLanguage(root) === "zh-CN" ? ZH_CN : EN;
  const template = table[key] || EN[key] || key;
  return template.replace(/\{(\w+)\}/g, (match, name) => {
    const value = values[name];
    return value === null || value === undefined ? match : String(value);
  });
}

function getFunctionLanguage(root: HostWindow): string | undefined {
  try {
    if (typeof root.CommonGetTranslationLanguage === "function") return root.CommonGetTranslationLanguage();
  } catch {
    /* Best effort only. */
  }
  return undefined;
}
